package com.caletux.javafundamental.operator;

public class OperatorAssignment {
	public static void main(String[] args) {

		int value = 2;
		int anotherValue = 19;
		System.out.println("Data pada value adalah --> " + value);
		System.out.println("Data pada anotherValue adalah -->" + anotherValue);

		/* Potongan kode di atas value merupakan left operand.
		Angka 2 merupakan sebuah nilai integer (right operand).
		Sesuai dengan penjelasan operasi assignment, nilai value
		akan menjadi nilai integer 2. Operasi ini bisa juga disebut
		sebagai copy, karena nilai left operand akan mengkopi nilai
		right operand. */
	}
}
