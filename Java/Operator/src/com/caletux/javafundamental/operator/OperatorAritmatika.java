package com.caletux.javafundamental.operator;

public class OperatorAritmatika {
	public static void main(String[] args) {

		// Operator Aritmatika
		System.out.println("Operasi Penjumlahan");
		int hasilPenjumlahan = 10 + 4;
		System.out.println("Hasil dari 10 + 4 adalah " + hasilPenjumlahan);
		System.out.println("======================================");

		System.out.println("Operasi Pengurangan");
		int hasilPengurangan = 10 - 4;
		System.out.println("Hasil dari 10 - 4 adalah " + hasilPengurangan);
		System.out.println("======================================");

		System.out.println("Operasi Pengalian");
		int hasilPengalihan = 10 * 4;
		System.out.println("Hasil dari 10 * 4 adalah " + hasilPengalihan);
		System.out.println("======================================");

		System.out.println("Operasi Pembagian");
		int hasilPembagian = 10 / 5;
		System.out.println("Hasil dari 10 : 4 adalah " + hasilPembagian);
		System.out.println("======================================");

		System.out.println("Operasi Hasil Bagi");
		int hasilBagi = 10 % 4;
		System.out.println("Hasil dari 10 % 4 adalah " + hasilBagi);
		System.out.println("======================================");

		System.out.println("Operasi Hasil BagiGanjil");
		int hasilBagiGanjil = 13 % 4;
		System.out.println("Hasil dari 13 % 4 adalah " + hasilBagiGanjil);
		System.out.println("======================================");
	}
}
